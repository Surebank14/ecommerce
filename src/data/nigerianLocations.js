// Get all states
export const getStates = () => Object.keys(nigerianLocations).sort();

// Get LGAs for a state
export const getLGAs = (state) => {
  if (!state || !nigerianLocations[state]) return [];
  return Object.keys(nigerianLocations[state]).sort();
};

// Get Towns for a state and LGA
export const getTowns = (state, lga) => {
  if (!state || !lga || !nigerianLocations[state] || !nigerianLocations[state][lga]) return [];
  return nigerianLocations[state][lga].sort();
};

